void solve()
{
	int n, m;
	cin >> n >> m;

	int d = ask(1, 1);
	int x1 = min(n, 1 + d), y1 = max(1, (d + 2 - x1));
	int y2 = min(m, 1 + d), x2 = max(1, (d + 2 - y2));

	int resd1 = ask(x1, y1);
	int resd2 = ask(x2, y2);

	if (resd1 == 0) {
		cout << "! " << x1 << ' ' << y1 << endl;
		cout.flush();
		return;
	}
	if (resd2 == 0) {
		cout << "! " << x2 << ' ' << y2 << endl;
		cout.flush();
		return;
	}
	int ansx2 = x2 + (resd2 / 2), ansy2 = y2 - (resd2 / 2);
	int ansx1 = x1 - (resd1 / 2), ansy1 = y1 + (resd1 / 2);

	if (ask(ansx1, ansy1) == 0) {
		cout << "! " << ansx1 << ' ' << ansy1 << endl;
		cout.flush();
	}
	else {
		cout << "! " << ansx2 << ' ' << ansy2 << endl;
		cout.flush();
	}
}
